public class U19Polymorphism {
    public static void main(String[] args) {
//Polymorphism
//
//Overriden ve overriding method’larin ikisini de kullanmak istersek child class’da
// (overriding method) super keyword’unu kullanabiliriz.
//
//	Polymorphism = Overloading + Overriding
//
//➢ Poly “çok” morph ise “form”, “biçim” anlamlarını taşır.
// Bu ikisinin birleşimiyle oluşan “polymorphism” sözcüğü “çok biçimlilik” anlamına gelir.
//
//➢ Özetle, oluşturulan nesnelerin gerektiğinde kılıktan kılığa girip başka bir nesneymiş gibi
// davranabilmesine polymorphism diyebiliriz.
// Bunlar program kodlarının yeniden kullanılabilmesi veya var olan kodun geliştirilebilmesi açısından
// çok önemlidir.
//
//➢ Method Overloading bir compile time (static) polymorphism’dir.
// Method Overloading sayesinde ayni isme, ayni body’e, farkli parametrelere sahip
// bir cok method uretip kullanabiliriz.
//
//➢ Method Overriding bir run time (dynamic) polymorphism’dir.
// Method Overriding sayesinde ayni isme, ayni parametrelere’e,
// farkli body’e sahip bir cok method uretip kullanabiliriz.
    }
    //POLYMORPHISM=Overloading+Overriding
    //
    //	*Olusturulan nesnelerin gerektiginde kilik degistirip baska bir nesneymis gibi davranmasidir.
    //-------------------------------------------------------------------------------------------------------
}
